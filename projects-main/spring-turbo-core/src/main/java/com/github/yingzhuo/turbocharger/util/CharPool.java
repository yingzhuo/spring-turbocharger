package com.github.yingzhuo.turbocharger.util;

/**
 * 字符池
 *
 * @author 应卓
 * @see StringPool
 * @since 3.5.0
 */
public interface CharPool {

	char SPACE = ' ';
	char TAB = '\t';
	char DOT = '.';
	char SLASH = '/';
	char BACKSLASH = '\\';
	char CR = '\r';
	char LF = '\n';
	char QUESTION_MARK = '?';
	char HYPHEN = '-';
	char UNDERSCORE = '_';
	char COMMA = ',';
	char SINGLE_QUOTE = '\'';
	char DOUBLE_QUOTE = '"';
	char AT_SIGN = '@';
	char SEMICOLON = ';';
	char COLON = ':';

}
