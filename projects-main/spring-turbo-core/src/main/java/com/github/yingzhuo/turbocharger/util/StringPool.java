package com.github.yingzhuo.turbocharger.util;

/**
 * 字符串池
 *
 * @author 应卓
 * @see CharPool
 * @since 3.5.0
 */
public interface StringPool {

	String EMPTY = "";
	String SPACE = " ";
	String TAB = "\t";
	String COLON = ":";
	String SEMICOLON = ";";
	String DOT = ".";
	String DOT_DOT = "..";
	String SLASH = "/";
	String BACKSLASH = "\\";
	String CR = "\r";
	String LF = "\n";
	String QUESTION_MARK = "?";
	String HYPHEN = "-";
	String UNDERSCORE = "_";
	String COMMA = ",";
	String SHARP = "#";

	String OK = "OK";
	String NG = "NG";

}
